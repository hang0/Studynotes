# python中的进制转换方法
# 十进制转二进制
print(bin(22))
# 十进制转八进制
print(oct(22))
# 十进制转十六进制
print(hex(22))
# 二进制转十进制
print(int('0b10110',2))
# 八进制转十进制
print(int('0o26',8))
# 十六进制转十进制
print(int('0x16',16))

num = float('123.3')
print(num,type(num))

